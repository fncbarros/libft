int	ft_atoi(char *str)
{
	int i;
	int j;
	int s;

	i = 0;
	s = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s++;
		i++;
	}
	j = 0;
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		j *= 10;
		j += (str[i] - '0');
		i++;
	}
	if ((s % 2) != 0)
		j *= -1;
	return (j);
}
