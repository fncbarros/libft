int	ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[0] == '\0')
		{
			return (1);
		}
		else if (str[i] >= '1' && str[i] <= '9')
		{
			i++;
		}
		else
			return (0);
	}
	return (1);
}
