char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *a;
	char *b;

	a = dest;
	b = src;
	if (n != 0)
	{
		while (1)
		{
			if ((*a++ = *b++) == 0)
			{
				while (--n != 0)
					*a++ = 0;
				break ;
			}
			if (--n == 0)
				break ;
		}
	}
	return (dest);
}
