#include <unistd.h>

void	ft_putnbr(int nb)
{
	char a;
	long i;

	i = nb;
	if (i < 0)
	{
		write(1, "-", 1);
		i = i * (-1);
	}
	if ((i / 10) > 0)
	{
		ft_putnbr(i / 10);
	}
	a = (i % 10) + '0';
	write(1, &a, 1);
}
